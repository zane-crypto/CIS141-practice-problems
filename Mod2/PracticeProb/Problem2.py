'''
2. Create a program that prompts for 2 numbers and 
then outputs the addition, subtraction, multiplication,
and division of the first number by the second number.
'''

num1 = int(input("Please enter a number: "))
num2 = int(input("Please enter another number: "))

print(num1+num2)
print(num1-num2)
print(num1*num2)
print(num1/num2)
